var a = 1;
console.log(a);
a = 3;
console.log("Value of the variable after changing: ", a);

//same code with let
let b = 2;
console.log(b);
b = 4;
console.log("Value of the variable after changing: ", b);

//same code with const
const c = 2;
console.log(c);
// c = 4; //const value can't be changed throughout the lifetime of the program
console.log("Value of the variable after changing: ", c);

